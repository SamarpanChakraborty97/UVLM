!*********************************************************************!
      subroutine error (er , X , XC , Dof)
!*********************************************************************!
!                           DESCRIPTION                               !
!=====================================================================!
!                             MODULES                                 !
!=====================================================================!
!                        VARIABLE DECLARATION                         !      
      implicit none
      
      integer                                            :: i, Dof
      
      doubleprecision, dimension( Dof )                  :: X, XC, E
      
      doubleprecision                                    :: er
 
!=====================================================================!
!                       BODY OF SUBROUTINE                            !
      do i = 1, DoF
               
        E(i) =  abs ( X(i) - XC(i) )
      
      end do
      
      er = MAXVAL( E )
!----------------------------------------------------------------------      
      end subroutine error